public class test_10 {
    public static void main(String[] args) {
      int i = 10;
      double d = i; // implicit type casting from int to double
      System.out.println(d); // prints 10.0
      char c = 'A';
      int asciiValue = c; // implicit type casting from char to int
      System.out.println(asciiValue); // prints 65
    }
  }
  